export function isAppLanguage(value: unknown): value is AppLanguage {
  return APP_LANGUAGES.some((language) => language.id === value);
}

export function getTranslator(language?: AppLanguage): TranslateFunction {
  const dictionary = TRANSLATIONS[language ?? "zh-CN"] ?? zhCN;

  return (key, values = {}) => {
    const template = dictionary[key] ?? zhCN[key] ?? key;
    return Object.entries(values).reduce(
      (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
      template
    );
  };
}
